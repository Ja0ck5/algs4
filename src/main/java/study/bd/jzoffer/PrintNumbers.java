public class PrintNumbers {

    public int[] printNumbers(int n) {
        int max = 1;
        while(n-- > 0){
            max *= 10;
        }
        int[] res = new int[max - 1];
        for (int i = 0; i < res.length; i++) {
            res[i] = i + 1;
        }
        return res;
    }

}
